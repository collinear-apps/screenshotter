/**
 * Infer the CRUD role + resource for a fixture, so the stateful store can serve
 * it. A GET on a collection path ("/api/models") is a `list`; a GET on a
 * detail path ("/api/models/{id}") is a `get`; POST=create, PUT/PATCH=update,
 * DELETE=delete on the matching resource. Resource = last static segment.
 */
function deriveCrud(f: ApiFixture): { resource?: string; crud?: MockRoute['crud'] } {
  const segs = f.pathTemplate.split('/').filter((s) => s.length > 0);
  if (segs.length === 0) return {};
  const endsWithParam = /^\{[^}]+\}$/.test(segs[segs.length - 1]);
  // Resource name = last NON-param segment.
  let resource: string | undefined;
  for (let i = segs.length - 1; i >= 0; i--) {
    if (!/^\{[^}]+\}$/.test(segs[i])) { resource = segs[i]; break; }
  }
  if (!resource) return {};
  // GraphQL / streaming endpoints are not REST resources.
  if (f.graphqlOperation || f.isStream) return {};

  const method = f.method.toUpperCase();
  if (method === 'GET') return { resource, crud: endsWithParam ? 'get' : 'list' };
  if (method === 'POST' && !endsWithParam) return { resource, crud: 'create' };
  if ((method === 'PUT' || method === 'PATCH') && endsWithParam) return { resource, crud: 'update' };
  if (method === 'DELETE' && endsWithParam) return { resource, crud: 'delete' };
  return {};
}

/** Re-derive the matchQuery object from a fixture's query signature. */
function matchQueryOf(f: ApiFixture): Record<string, string> | undefined {
  if (!f.querySignature) return undefined;
  const out: Record<string, string> = {};
  for (const part of f.querySignature.split('&')) {
    const eq = part.indexOf('=');
    if (eq === -1) continue;
    out[part.slice(0, eq)] = part.slice(eq + 1);
  }
  return Object.keys(out).length > 0 ? out : undefined;
}

/** Map fixtures → MockRoutes (request-aware + stateful + streaming metadata). */
export function fixturesToRoutes(fixtures: ApiFixture[]): MockRoute[] {
  return fixtures.map((f) => {
    const { resource, crud } = deriveCrud(f);
    const route: MockRoute = {
      method: f.method,
      pathTemplate: f.pathTemplate,
      status: f.status,
      contentType: f.contentType,
      bodyFile: `../fixtures/${f.file}`,
    };
    const mq = matchQueryOf(f);
    if (mq) route.matchQuery = mq;
    if (f.requestBodyHash) route.matchBodyHash = f.requestBodyHash;
    if (f.graphqlOperation) route.matchOperation = f.graphqlOperation;
    if (f.responseHeaders) route.responseHeaders = f.responseHeaders;
    if (f.isStream) route.isStream = true;
    if (resource) route.resource = resource;
    if (crud) route.crud = crud;
    return route;
  });
}

export async function writeMockServer(
  fixtures: ApiFixture[],
  mockDir: string,
  seeds: MockSeedEntity[] = [],
): Promise<void> {
  await mkdir(mockDir, { recursive: true });

  const routes = fixturesToRoutes(fixtures);

  const pkg = {
    name: 'screenshotter-mock',
    private: true,
    type: 'module',
    scripts: { start: 'node server.mjs' },
  };

  await Promise.all([
    writeFile(path.join(mockDir, 'routes.json'), JSON.stringify(routes, null, 2), 'utf8'),
    writeFile(path.join(mockDir, 'seeds.json'), JSON.stringify(seeds, null, 2), 'utf8'),
    writeFile(path.join(mockDir, 'server.mjs'), SERVER_MJS, 'utf8'),
    writeFile(path.join(mockDir, 'package.json'), JSON.stringify(pkg, null, 2), 'utf8'),
    writeFile(path.join(mockDir, 'README.md'), readme(routes.length, seeds.length), 'utf8'),
  ]);
}

